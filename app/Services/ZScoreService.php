     * Jika nilai > Median:  Z = (nilai - Median) / (SD_plus1 - Median)
     * Format SD array: [-3SD, -2SD, -1SD, Median, +1SD, +2SD, +3SD]
     */
    public static function hitungZScore(float $nilai, array $sd): float
    {
        $median  = $sd[3];
        $sdMinus = $sd[2]; // -1 SD
        $sdPlus  = $sd[4]; // +1 SD

        if ($nilai <= $median) {
            if ($median - $sdMinus == 0) return 0;
            return round(($nilai - $median) / ($median - $sdMinus), 2);
        } else {
            if ($sdPlus - $median == 0) return 0;
            return round(($nilai - $median) / ($sdPlus - $median), 2);
        }
    }

    /** Ambil data SD untuk BB/U */
    public static function getSDtabelBBU(int $umurBulan, string $jk): ?array
    {
        $jk  = self::normalizeJK($jk);
        $key = "{$umurBulan}{$jk}";
        return self::$bbu[$key] ?? null;
    }

    /** Ambil data SD untuk TB/U */
    public static function getSDtabelTBU(int $umurBulan, string $jk): ?array
    {
        $jk  = self::normalizeJK($jk);
        $key = "{$umurBulan}{$jk}";
        return self::$tbu[$key] ?? null;
    }

    /**
     * Ambil data SD untuk BB/TB
     * Tinggi badan dibulatkan ke 0.5 terdekat
     */
    public static function getSDtabelBBTB(float $tinggi, string $jk): ?array
    {
        $jk      = self::normalizeJK($jk);
        $rounded = round($tinggi * 2) / 2; // bulatkan ke 0.5
        // Format key pakai koma (,) untuk desimal sesuai Excel
        $key = $rounded == (int)$rounded
            ? (string)(int)$rounded . $jk
            : str_replace('.', ',', number_format($rounded, 1)) . $jk;
        return self::$bbtb[$key] ?? null;
    }

    /** Ambil data SD untuk IMT/U */
    public static function getSDtabelIMTU(int $umurBulan, string $jk): ?array
    {
        $jk  = self::normalizeJK($jk);
        $key = "{$umurBulan}{$jk}";
        return self::$imtu[$key] ?? null;
    }

    /** Normalisasi jenis kelamin ke format tabel */
    private static function normalizeJK(string $jk): string
    {
        $jk = strtolower(trim($jk));
        if (str_contains($jk, 'laki')) return 'Laki - Laki';
        return 'Perempuan';
    }

    /** Status gizi BB/U berdasarkan Z-Score */
    public static function statusBBU(float $z): string
    {
        if ($z < -3)      return 'Berat Badan Sangat Kurang';
        if ($z < -2)      return 'Berat Badan Kurang';
        if ($z <= 1)      return 'Berat Badan Normal';
        if ($z <= 2)      return 'Risiko Berat Badan Lebih';
        return 'Berat Badan Lebih';
    }

